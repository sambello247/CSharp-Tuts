using System;

namespace math2
{
    class Program
    {
        static void Main(string[] args)
        {
            int x = 9;
            int y = 4;

            // Max
            Console.WriteLine("Max : " + Math.Max(x,y));

            // Min
            Console.WriteLine("Min : " + Math.Min(x, y));

            // Sqrt
            Console.WriteLine("SQRT : " + Math.Sqrt(x));

            //Pow
            Console.WriteLine("Power : " + Math.Pow(3,2));

            //PI
            Console.WriteLine("PI : " + Math.PI);
        }
    }
}
